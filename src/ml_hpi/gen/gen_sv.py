"""SystemVerilog code generator for ml-hpi."""
from __future__ import annotations
import os
from io import StringIO
from pathlib import Path
from typing import Union

from ml_hpi.model import Interface, Method, MlHpiDoc
from .gen_base import Generator


class GenSV(Generator):
    """Generates:
      - {flat_root}_dpi_pkg.sv : Root class (outside any package) +
                                  DPI exports/imports package
    """

    def __init__(self, doc: MlHpiDoc, root_if: str, addr_bits: int = 64):
        super().__init__(doc)
        self.root_if = root_if          # fully-qualified root interface name
        self.addr_bits = addr_bits

    def generate(self, outdir: Union[str, Path]) -> list[Path]:
        outdir = Path(outdir)
        outdir.mkdir(parents=True, exist_ok=True)

        root_iface = self._find_iface(self.root_if)
        flat = self.pkg_to_flat(self.root_if)

        out = StringIO()
        out.write(f"// Auto-generated by ml-hpi GenSV — do not edit\n\n")
        # Root class and DPI package are all in one package block so the
        # Root class is visible to navigate() inside the same package.
        self._write_dpi_package(out, root_iface, flat)

        outfile = outdir / f"{flat}_dpi_pkg.sv"
        outfile.write_text(out.getvalue())
        return [outfile]

    # ------------------------------------------------------------------

    def _find_iface(self, name: str) -> Interface:
        for iface in self.doc.spec.interfaces:
            if iface.name == name:
                return iface
        raise ValueError(f"Interface '{name}' not found in spec")

    def _all_interfaces_for_root(self, root: Interface) -> list[Interface]:
        """All interfaces reachable from root (depth-first, declaration order)."""
        seen = set()
        result = []
        def visit(iface: Interface):
            if iface.name in seen:
                return
            seen.add(iface.name)
            result.append(iface)
            for m in iface.members:
                visit(self._find_iface(m.type))
        visit(root)
        return result

    # ------------------------------------------------------------------
    # Root class (declared at file scope, outside any package)
    # ------------------------------------------------------------------

    def _write_root_class(self, out: StringIO, root: Interface, flat_root: str):
        sv_name = self.sv_qualified(root.name)
        out.write(f"  class {flat_root}Root;\n")
        out.write(f"    static {sv_name} __registry[int];\n")
        out.write(f"    static int         __next_id = 0;\n\n")
        out.write(f"    static function int register_inst({sv_name} impl);\n")
        out.write(f"      int id = __next_id++;\n")
        out.write(f"      __registry[id] = impl;\n")
        out.write(f"      return id;\n")
        out.write(f"    endfunction\n\n")
        out.write(f"    static function {sv_name} get(int root_id);\n")
        out.write(f"      return __registry[root_id];\n")
        out.write(f"    endfunction\n")
        out.write(f"  endclass\n")

    # ------------------------------------------------------------------
    # DPI package
    # ------------------------------------------------------------------

    def _write_dpi_package(self, out: StringIO, root: Interface, flat_root: str):
        all_ifaces = self._all_interfaces_for_root(root)

        blocking_methods = [(i, m) for i in all_ifaces
                            for m in i.methods if m.is_blocking()]

        out.write(f"package {flat_root}_dpi;\n\n")

        # Root class lives inside the package so navigate() can see it
        self._write_root_class(out, root, flat_root)
        out.write("\n")
        if blocking_methods:
            out.write(f"  // Completion imports — C provides, SV calls on task finish\n")
            for iface, meth in blocking_methods:
                flat_if = self.pkg_to_flat(iface.name)
                args = ["chandle cb"]
                if meth.rtype != "void":
                    args.append(f"{self.sv_type(meth.rtype, self.addr_bits)} rval")
                out.write(f'  import "DPI-C" function void '
                          f'{flat_if}_{meth.name}_complete({", ".join(args)});\n')
            out.write("\n")

        # Navigate function — returns root interface type
        # For simple leaf roots this is a direct lookup.
        # For composite roots we walk members depth-first.
        self._write_navigate(out, root, flat_root)
        out.write("\n")

        # Method exports
        out.write(f"  // Method exports — SV provides, C calls\n")
        for iface in all_ifaces:
            for meth in iface.methods:
                flat_if = self.pkg_to_flat(iface.name)
                self._write_export(out, iface, meth, flat_if, flat_root)

        out.write(f"endpackage\n")

    def _write_navigate(self, out: StringIO, root: Interface, flat_root: str):
        sv_name = self.sv_qualified(root.name)
        out.write(f"  // Navigate root_id + path to the target interface instance\n")
        out.write(f"  function automatic {sv_name} navigate(int root_id, int path);\n")
        out.write(f"    automatic {sv_name} root = {flat_root}Root::get(root_id);\n")

        if not root.members:
            out.write(f"    return root;\n")
        else:
            offset = 0
            for mem in root.members:
                if mem.kind == "field":
                    out.write(f"    if (path == {offset}) return root.{mem.name}();\n")
                    offset += 1
                else:
                    out.write(f"    if (path == {offset}) return root.{mem.name}_at(0);\n")
                    out.write(f"    path -= {offset + 1};\n")
                    out.write(f"    return root.{mem.name}_at(path);\n")
                    offset += 1
            out.write(f"    return null;\n")

        out.write(f"  endfunction\n")

    def _write_export(self, out: StringIO, iface: Interface, meth: Method,
                      flat_if: str, flat_root: str):
        func_name = f"{flat_if}_{meth.name}"
        params = ["int root_id", "int path"]
        for p in meth.params:
            params.append(f"{self.sv_type(p.type, self.addr_bits)} {p.name}")
        if meth.is_blocking():
            params.append("chandle cb")

        out.write(f'  export "DPI-C" function {func_name};\n')
        out.write(f"  function automatic void {func_name}({', '.join(params)});\n")
        sv_iface = self.sv_qualified(iface.name)
        out.write(f"    automatic {sv_iface} iface = navigate(root_id, path);\n")

        call_args = ", ".join(p.name for p in meth.params)

        if meth.is_blocking():
            out.write(f"    fork begin\n")
            if meth.rtype != "void":
                sv_rt = self.sv_type(meth.rtype, self.addr_bits)
                out.write(f"      automatic {sv_rt} rval;\n")
                out.write(f"      iface.{meth.name}(rval{', ' + call_args if call_args else ''});\n")
                out.write(f"      {flat_if}_{meth.name}_complete(cb, rval);\n")
            else:
                out.write(f"      iface.{meth.name}({call_args});\n")
                out.write(f"      {flat_if}_{meth.name}_complete(cb);\n")
            out.write(f"    end join_none\n")
        else:
            if meth.rtype != "void":
                out.write(f"    return iface.{meth.name}({call_args});\n")
            else:
                out.write(f"    iface.{meth.name}({call_args});\n")

        out.write(f"  endfunction\n\n")
