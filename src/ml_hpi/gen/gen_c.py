"""C header generator for ml-hpi (C-to-SV DPI path)."""
from __future__ import annotations
from io import StringIO
from pathlib import Path
from typing import Union

from ml_hpi.model import Interface, Method, MlHpiDoc
from .gen_base import Generator


class GenC(Generator):
    """Generates a C header declaring:
      - extern declarations for DPI export functions (SV provides, C calls)
      - prototype declarations for completion import functions (C provides, SV calls)
    """

    def __init__(self, doc: MlHpiDoc, root_if: str, addr_bits: int = 64):
        super().__init__(doc)
        self.root_if = root_if
        self.addr_bits = addr_bits

    def generate(self, outdir: Union[str, Path]) -> list[Path]:
        outdir = Path(outdir)
        outdir.mkdir(parents=True, exist_ok=True)

        flat = self.pkg_to_flat(self.root_if)
        root_iface = self._find_iface(self.root_if)
        all_ifaces = self._all_interfaces_for_root(root_iface)

        out = StringIO()
        pkg_name = f"{flat}_dpi"
        guard = f"{flat.upper()}_DPI_H"
        out.write(f"/* Auto-generated by ml-hpi GenC — do not edit */\n")
        out.write(f"#ifndef {guard}\n")
        out.write(f"#define {guard}\n\n")
        out.write(f"#include <stdint.h>\n")
        out.write(f"#include <stdbool.h>\n")
        out.write(f"#include \"svdpi.h\"\n\n")
        out.write(f"#ifdef __cplusplus\nextern \"C\" {{\n#endif\n\n")

        # DPI export declarations (SV provides, C calls)
        out.write(f"/* DPI exports — implemented in SystemVerilog, called from C */\n")
        for iface in all_ifaces:
            for meth in iface.methods:
                flat_if = self.pkg_to_flat(iface.name)
                self._write_export_decl(out, iface, meth, flat_if)

        out.write(f"\n/* Completion imports — implemented in C, called from SystemVerilog */\n")
        for iface in all_ifaces:
            for meth in iface.methods:
                if meth.is_blocking():
                    flat_if = self.pkg_to_flat(iface.name)
                    self._write_completion_decl(out, meth, flat_if)

        # Package init and scope helper.
        # C implements both {pkg_name}_init() and {pkg_name}_set_scope().
        # init() stores svGetScope() at elaboration; set_scope() calls svSetScope
        # before any DPI export invocation.
        out.write(f"\n/* Package init — C must implement:\n")
        out.write(f" *   static svScope s_{pkg_name}_scope;\n")
        out.write(f" *   int {pkg_name}_init(void) {{ s_{pkg_name}_scope = svGetScope(); return 0; }}\n")
        out.write(f" *   void {pkg_name}_set_scope(void) {{ svSetScope(s_{pkg_name}_scope); }}\n")
        out.write(f" */\n")
        out.write(f"int  {pkg_name}_init(void);\n")
        out.write(f"void {pkg_name}_set_scope(void);\n")

        out.write(f"\n#ifdef __cplusplus\n}}\n#endif\n\n")
        out.write(f"#endif /* {guard} */\n")

        outfile = outdir / f"{flat}_dpi.h"
        outfile.write_text(out.getvalue())
        return [outfile]

    # ------------------------------------------------------------------

    def _find_iface(self, name: str) -> Interface:
        for iface in self.doc.spec.interfaces:
            if iface.name == name:
                return iface
        raise ValueError(f"Interface '{name}' not found in spec")

    def _all_interfaces_for_root(self, root: Interface) -> list[Interface]:
        seen = set()
        result = []
        def visit(iface):
            if iface.name in seen:
                return
            seen.add(iface.name)
            result.append(iface)
            for m in iface.members:
                visit(self._find_iface(m.type))
        visit(root)
        return result

    def _write_export_decl(self, out: StringIO, iface: Interface, meth: Method,
                           flat_if: str):
        func_name = f"{flat_if}_{meth.name}"
        params = ["int root_id", "int path"]
        for p in meth.params:
            params.append(f"{self.c_type(p.type, self.addr_bits)} {p.name}")
        if meth.is_blocking():
            params.append("void *cb")
        out.write(f"extern void {func_name}({', '.join(params)});\n")

    def _write_completion_decl(self, out: StringIO, meth: Method, flat_if: str):
        func_name = f"{flat_if}_{meth.name}_complete"
        params = ["void *cb"]
        if meth.rtype != "void":
            params.append(f"{self.c_type(meth.rtype, self.addr_bits)} rval")
        out.write(f"void {func_name}({', '.join(params)});\n")
